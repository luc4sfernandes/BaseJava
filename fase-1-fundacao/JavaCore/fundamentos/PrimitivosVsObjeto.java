public class PrimitivosVsObjeto{

	public static void main(String[] args){

		// Objetos //

		String s = "texto"; // Criação de um objeto
		s.toUpperCase(); // Um Objeto tem caracteristas e comportamentos

		// Tipos primitivos //

		int a = 123; // Não contem comportamentos!!!
		// a.toUpperCase(); --> Invalido!!!
		System.out.println(a);

		// Existe uma versão 'Objeto dos tipos primitivos' --> Wrappers

	}
}
